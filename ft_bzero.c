#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	void	*potr;

	i = 0;
	potr = s;
	while (i < n)
	{
		*(unsigned char *)potr++ = '\0';
		i++;
	}
}
